public class Car {
    private int wheels;
    private int speed;
    private String cab;
    private String turnOnMusic;
    private String outdrive;

    public int getWheels() {
        return wheels;
    }

    public int getSpeed() {
        return speed;
    }

    public String getCab() {
        return cab;
    }

    public String getTurnOnMusic() {
        return turnOnMusic;
    }

    public void setOutdrive(String outdrive) {
        this.outdrive = outdrive;
    }

    public void setWheels(int wheels) {
        this.wheels = wheels;
    }

    public void setSpeed(int speed) {
        this.speed = speed;
    }

    public void setTurnOnMusic(String turnOnMusic) {
        this.turnOnMusic = turnOnMusic;
    }

    public void setCab(String cab) {
        this.cab = cab;
    }
}
